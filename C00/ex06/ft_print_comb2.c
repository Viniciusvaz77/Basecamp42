#include <unistd.h>

void	ft_print_comb2(void)
{
	int		ct1;
	int		ct2;
	char	c;

	write(1, "00 01", 5);
	ct1 = 0;
	ct2 = 2;
	while (ct1 < 100 && ct2 < 100)
	{
		write(1, ", ", 2);
		c = ct1 / 10 + '0';
		write(1, &c, 1);
		c = ct1 % 10 + '0';
		write(1, &c, 1);
		write(1, " ", 1);
		c = ct2 / 10 + '0';
		write(1, &c, 1);
		c = ct2 % 10 + '0';
		write(1, &c, 1);
		++ct2;
		if (ct2 == 100)
		{
			ct2 = ++ct1 + 1;
		}
	}
}
